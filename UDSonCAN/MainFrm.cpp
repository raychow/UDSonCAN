BOOL CMainFrame::CreateDockingWindows()
{
	BOOL bNameValid;

	// 创建服务视图
	CString strServiceView;
	bNameValid = strServiceView.LoadString(IDS_SERVICEVIEW);
	ASSERT(bNameValid);
	if (!m_wndServiceView.Create(strServiceView, this, CRect(0, 0, 200, 200), TRUE, ID_VIEW_SERVICEVIEW, WS_CHILD | WS_VISIBLE | WS_CLIPSIBLINGS | WS_CLIPCHILDREN | CBRS_LEFT | CBRS_FLOAT_MULTI))
	{
		TRACE0("未能创建服务窗口\n");
		return FALSE; // 未能创建
	}

	// 创建监视窗口
	CString strNetworklayerWatchWnd;
	bNameValid = strNetworklayerWatchWnd.LoadString(IDS_NETWORKLAYERWATCH_WND);
	ASSERT(bNameValid);
	if (!m_wndNetworkLayerWatch.Create(strNetworklayerWatchWnd, this, CRect(0, 0, 100, 100), TRUE, ID_VIEW_NETWORKLAYERWATCHWND, WS_CHILD | WS_VISIBLE | WS_CLIPSIBLINGS | WS_CLIPCHILDREN | CBRS_BOTTOM | CBRS_FLOAT_MULTI))
	{
		TRACE0("未能创建网络层监视窗口\n");
		return FALSE; // 未能创建
	}

	CString strApplicationLayerWnd;
	bNameValid = strApplicationLayerWnd.LoadString(IDS_APPLICATIONLAYERWATCH_WND);
	ASSERT(bNameValid);
	if (!m_wndApplicationLayerWatch.Create(strApplicationLayerWnd, this, CRect(0, 0, 100, 100), TRUE, ID_VIEW_APPLICATIONLAYERWATCHWND, WS_CHILD | WS_VISIBLE | WS_CLIPSIBLINGS | WS_CLIPCHILDREN | CBRS_BOTTOM | CBRS_FLOAT_MULTI))
	{
		TRACE0("未能创建应用层监视窗口\n");
		return FALSE; // 未能创建
	}

	SetDockingWindowIcons(theApp.m_bHiColorIcons);
	return TRUE;
}

